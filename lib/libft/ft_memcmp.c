#include "libft.h"
#include <stdio.h>

int	ft_memcmp(const void *src1, const void *src2, size_t n)
{
	char	*s1;
	char	*s2;
	size_t	i;

	s1 = (char *) src1;
	s2 = (char *) src2;
	i = 0;
	while (i < n)
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return (0);
}
